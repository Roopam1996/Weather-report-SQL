CREATE TABLE STATS
(
ID INT(10),
MONTH INT(2), CHECK (MONTH BETWEEN 1 AND 12),
TEMP_F DECIMAL (5,2), CHECK (TEMP_F BETWEEN -80 AND 150),
RAIN_I DECIMAL(5,2), CHECK (RAIN_I BETWEEN 0 AND 100),
PRIMARY KEY (ID, MONTH)
);
