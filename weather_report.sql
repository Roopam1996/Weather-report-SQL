SELECT STATION.CITY,STATS.MONTH, STATS.TEMP_F 
FROM STATION JOIN STATS ON STATION.ID= STATS.ID;


SELECT  STATS.ID, STATION.CITY, STATS.MONTH, STATS.RAIN_I AS Greatest_Rainfall
FROM STATS JOIN STATION ON STATS.ID = STATION.ID ORDER BY STATS.RAIN_I DESC;

SELECT STATION.CITY, STATION.LAT_N, STATS.MONTH, STATS.TEMP_F AS TEMPERATURE 
FROM STATS JOIN STATION ON STATS.ID= STATION.ID 
WHERE STATS.MONTH= 7 ORDER BY TEMP_F ASC;

SELECT STATION. CITY, MAX(STATS.TEMP_F) AS MAX_TEMPERATURE, MIN(STATS.TEMP_F) 
AS MIN_TEMPERATURE, AVG(STATS.RAIN_I) AS AVERAGE_RAINFALL 
FROM STATS JOIN STATION ON STATS.ID= STATION.ID GROUP BY STATION.CITY;

SELECT STATION.CITY, STATS.MONTH,((STATS.TEMP_F - 32) * 5/9) AS Temperature_Celsius,
(STATS.RAIN_I * 2.54) AS Rainfall_Centimeter FROM STATS JOIN STATION ON STATS.ID= STATION.ID;

